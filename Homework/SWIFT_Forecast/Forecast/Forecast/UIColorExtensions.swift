import UIKit

extension UIColor {
    
    class func sunnyColor() -> UIColor {
        return UIColor(red: 255.0/255.0, green: 255.0/255.0, blue: 143.0/255.0, alpha: 1.0)
    }
    
    class func snowyColor() -> UIColor {
        return UIColor(red: 201.0/255.0, green: 211.0/255.0, blue: 211.0/255.0, alpha: 1.0)
    }
    
    class func windyColor() -> UIColor {
        return UIColor(red: 205.0/255.0, green: 205.0/255.0, blue: 205.0/255.0, alpha: 1.0)
    }
    
    class func partlyCloudyNightColor() -> UIColor {
        return UIColor(red: 130.0/255.0, green: 130.0/255.0, blue: 130.0/255.0, alpha: 1.0)
    }
    
    class func cloudyColor() -> UIColor {
        return UIColor(red: 130.0/255.0, green: 130.0/255.0, blue: 130.0/255.0, alpha: 1.0)
    }
    
    class func partlyCloudyDayColor() -> UIColor {
        return UIColor(red: 89.0/255.0, green: 124.0/255.0, blue: 124.0/255.0, alpha: 1.0)
    }
    
    class func rainyColor() -> UIColor {
        return UIColor(red: 63.0/255.0, green: 104.0/255.0, blue: 104.0/255.0, alpha: 1.0)
    }
    
    class func sleetColor() -> UIColor {
        return UIColor(red: 63.0/255.0, green: 104.0/255.0, blue: 104.0/255.0, alpha: 1.0)
    }
    
    class func foggyColor() -> UIColor {
        return UIColor(red: 30.0/255.0, green: 30.0/255.0, blue: 30.0/255.0, alpha: 1.0)
    }
    
    class func nightColor() -> UIColor {
        return UIColor(red: 20.0/255.0, green: 20.0/255.0, blue: 20.0/255.0, alpha: 1.0)
    }
}